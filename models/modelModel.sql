CREATE TABLE models (
    id SERIAL PRIMARY KEY,
    ProjectName VARCHAR NOT NULL,
    PrimaryTechnology VARCHAR NOT NULL,
    ProjectDescription TEXT NOT NULL,
    MovingParts VARCHAR NOT NULL,
    ForceTorque VARCHAR NOT NULL,
    WeightConstraints VARCHAR NOT NULL,
    ElectronicsComplexity VARCHAR NOT NULL,
    PowerConsumption VARCHAR NOT NULL,
    SoftwareComplexity VARCHAR NOT NULL,
    MaterialDifficulty VARCHAR NOT NULL,
    ChemicalFluids VARCHAR NOT NULL,
    StructuralLoad VARCHAR NOT NULL,
    Aerodynamics VARCHAR NOT NULL,
    EnvironmentalConstraints VARCHAR NOT NULL,
    PredictedPrimaryField VARCHAR NOT NULL,
    PredictedComplexity VARCHAR NOT NULL,
    PredictedMechanicalContributions VARCHAR NOT NULL,
    PredictedElectricalContributions VARCHAR NOT NULL,
    PredictedCivilContributions VARCHAR NOT NULL,
    PredictedChemicalContributions VARCHAR NOT NULL,
    PredictedComputerContributions VARCHAR NOT NULL,
    PredictedSoftwareContributions VARCHAR NOT NULL,
    PredictedAerospaceContributions VARCHAR NOT NULL,
    PredictedMaterialsContributions VARCHAR NOT NULL,
    PredictedEnvironmentalContributions VARCHAR NOT NULL,
    PredictedIndustrialContributions VARCHAR NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);